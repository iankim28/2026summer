    "        _lbls_dual.append(yb.numpy())\n"
    "\n"
    "clean_probs_dual = {l: np.concatenate(clean_probs_dual[l]) for l in LANGS}\n"
    "_lbls_dual       = np.concatenate(_lbls_dual)\n"
    "\n"
    "# Eval PGD with PGD_STEPS=20 (stricter than training) against EN pair\n"
    "def pgd_eval_en(x, y, txt_en, eps, steps=PGD_STEPS):\n"
    "    x0 = x.clone().detach()\n"
    "    alpha = 2.5 * eps / steps\n"
    "    xadv = torch.clamp(x0 + torch.empty_like(x0).uniform_(-eps, eps), 0, 1).detach()\n"
    "    for _ in range(steps):\n"
    "        xadv.requires_grad_(True)\n"
    "        feat = encode_image_ml(xadv, \"en\")\n"
    "        loss = F.cross_entropy(logits_for(feat, txt_en), y)\n"
    "        g = torch.autograd.grad(loss, xadv)[0]\n"
    "        with torch.no_grad():\n"
    "            xadv = torch.min(torch.max(xadv + alpha * g.sign(), x0 - eps), x0 + eps)\n"
    "            xadv = torch.clamp(xadv, 0, 1)\n"
    "        xadv = xadv.detach()\n"
    "    return xadv\n"
    "\n"
    "print(\"Retention -- dual encoder pairs, PGD vs EN pair (20 steps):\")\n"
    "print(f\"{'eps':>5} | EN fooled | {'ko':>8} | {'es':>8} | {'fr':>8} | {'ja':>8}\")\n"
    "print(\"-\" * 57)\n"
    "\n"
    "adv_probs_dual = {}\n"
    "for eps in EPS_LIST:\n"
    "    with torch.no_grad():\n"
    "        TXT_LANG = build_txt_lang()\n"
    "\n"
    "    ep_probs  = {l: [] for l in LANGS}\n"
    "    ep_labels = []\n"
    "    for xb, yb in tloader:\n"
    "        xb, yb = xb.to(device), yb.to(device)\n"
    "        xb_adv = pgd_eval_en(xb, yb, TXT_LANG[\"en\"], eps=eps / 255)\n"
    "        with torch.no_grad():\n"
    "            batch_p = {\n"
    "                lang: torch.softmax(\n"
    "                    logits_for(encode_image_ml(xb_adv, lang), TXT_LANG[lang]), dim=-1\n"
    "                ).cpu().numpy()\n"
    "                for lang in LANGS\n"
    "            }\n"
    "        for lang in LANGS:\n"
    "            ep_probs[lang].append(batch_p[lang])\n"
    "        ep_labels.append(yb.cpu().numpy())\n"
