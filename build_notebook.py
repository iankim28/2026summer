    "    print(f\"{eps:>5} | {n_fooled:>9} | \" +\n"
    "          \" | \".join(f\"{ret[l]*100:>7.1f}%\" for l in [\"ko\", \"es\", \"fr\", \"ja\"]))\n"
    "\n"
    "print()\n"
