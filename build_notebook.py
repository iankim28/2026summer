# Cell 7 – architecture
cells.append(code(
    "# ── Image side: per-language CLS-token LoRA at every ViT block ───────────────\n"
    "#\n"
    "# The ViT uses NLD layout [batch, seq, d] with batch_first=True throughout.\n"
    "# CLS token: x[:, 0, :]   Injection: cat([cls_new.unsqueeze(1), x[:, 1:]], dim=1)\n"
    "#\n"
    "# CRITICAL FIX vs. Experiment C: Exp C manually permuted to LND (seq-first)\n"
    "# before passing to resblocks. With batch_first=True the resblocks expect NLD,\n"
    "# so Exp C's attention ran over batch items (seq=2) not tokens (seq=50) --\n"
    "# semantically wrong, which contributed to training collapse.\n"
    "# This implementation uses vit._embeds() which correctly returns NLD.\n"
    "\n"
    "class IntermediateLoRA(nn.Module):\n"
    "    def __init__(self, d=768, r=RANK):\n"
    "        super().__init__()\n"
    "        self.A = nn.Linear(d, r, bias=False)\n"
    "        self.B = nn.Linear(r, d, bias=False)\n"
    "        nn.init.kaiming_uniform_(self.A.weight)\n"
    "        nn.init.zeros_(self.B.weight)  # zero-init B: starts as identity\n"
    "\n"
    "    def forward(self, x):\n"
    "        return x + self.B(self.A(x))\n"
    "\n"
    "# 5 languages x 12 blocks = 60 adapters\n"
    "ml_adapters = nn.ModuleDict({\n"
    "    f\"{lang}_{i}\": IntermediateLoRA(d=768, r=RANK).to(device)\n"
    "    for lang in LANGS for i in range(12)\n"
    "})\n"
    "\n"
    "# ── Text side: per-language projection heads ──────────────────────────────────\n"
    "#\n"
    "# model.text is HFTextEncoder with MeanPooler -> 768-d -> model.text.proj (Sequential)\n"
    "# We bypass model.text.proj entirely and apply our own nn.Linear(768, 512) per lang.\n"
    "# Gradient flows through lang_text_proj[lang]; backbone (XLM-R) stays frozen.\n"
    "\n"
    "lang_text_proj = nn.ModuleDict({\n"
    "    lang: nn.Linear(768, 512, bias=False).to(device)\n"
    "    for lang in LANGS\n"
    "})\n"
    "for proj in lang_text_proj.values():\n"
    "    nn.init.kaiming_uniform_(proj.weight)\n"
    "\n"
    "total_img = sum(p.numel() for p in ml_adapters.parameters())\n"
    "total_txt = sum(p.numel() for p in lang_text_proj.parameters())\n"
    "print(f\"Trainable parameters: {total_img + total_txt:,}  ({(total_img+total_txt)/1e6:.2f}M)\")\n"
    "print(f\"  image LoRA adapters: {total_img:,}  (5 langs x 12 blocks x 2x(768x{RANK}))\")\n"
    "print(f\"  text proj heads:     {total_txt:,}  (5 langs x 768x512)\")\n"
    "print(f\"  frozen backbone:     {sum(p.numel() for p in model.parameters()):,}\")\n"
    "\n"
    "# ── encode_image_ml: ViT forward with per-language CLS injection ──────────────\n"
    "def encode_image_ml(x_pixel, lang):\n"
    "    vit = model.visual\n"
    "    x = vit._embeds((x_pixel - MEAN) / STD)          # [B, 50, 768] NLD\n"
    "    for i, block in enumerate(vit.transformer.resblocks):\n"
    "        x = block(x)                                   # [B, 50, 768]\n"
    "        cls_new = ml_adapters[f\"{lang}_{i}\"](x[:, 0, :])         # [B, 768]\n"
    "        x = torch.cat([cls_new.unsqueeze(1), x[:, 1:, :]], dim=1) # [B, 50, 768]\n"
    "    x = vit.ln_post(x[:, 0, :])   # [B, 768]  pool_type='tok'\n"
    "    if vit.proj is not None:\n"
    "        x = x @ vit.proj           # [B, 512]\n"
    "    return F.normalize(x, dim=-1)\n"
    "\n"
    "# ── encode_text_lang: frozen XLM-R backbone + trainable per-language head ─────\n"
    "# MeanPooler: masked mean of last_hidden_state -> [B, 768]\n"
    "# Backbone runs in no_grad; gradient flows through lang_text_proj[lang] only.\n"
    "def encode_text_lang(prompts, lang):\n"
    "    tokens = tokenizer(prompts).to(device)\n"
    "    attn_mask = (tokens != model.text.config.pad_token_id).long()\n"
    "    with torch.no_grad():\n"
    "        out = model.text.transformer(input_ids=tokens, attention_mask=attn_mask)\n"
    "        masked = out.last_hidden_state * attn_mask.unsqueeze(-1).float()\n"
    "        pooled = masked.sum(dim=1) / attn_mask.sum(-1, keepdim=True).float()  # [B, 768]\n"
    "    projected = lang_text_proj[lang](pooled)  # [B, 512] -- grad flows here\n"
    "    return F.normalize(projected, dim=-1)\n"
    "\n"
    "# ── build_txt_lang: pre-compute text embeddings from cached backbone features ──\n"
    "# txt_backbone is set in the training cell (pre-computed once before training).\n"
    "@torch.no_grad()\n"
    "def build_txt_lang():\n"
    "    return {\n"
    "        lang: F.normalize(lang_text_proj[lang](txt_backbone[lang]), dim=-1)\n"
    "        for lang in LANGS\n"
    "    }\n"
    "\n"
    "# ── Shape check ───────────────────────────────────────────────────────────────\n"
    "with torch.no_grad():\n"
    "    _d, _ = next(iter(get_loader(2, batch_size=2, seed=99)[0]))\n"
    "    _f = encode_image_ml(_d.to(device), \"en\")\n"
    "    print(f\"encode_image_ml output: {_f.shape}  (expected [2, 512])\")\n"
    "    del _d, _f\n"
    "print(\"Architecture ready.\")",
    "cell-arch"))

# Cell 8 – training
cells.append(code(
    "dual_loader, _ = get_loader(DUAL_TRAIN_N, batch_size=BATCH, seed=42, split=\"train\")\n"
    "tloader,     _ = get_loader(N_TRANSFER, seed=1)\n"
    "\n"
    "optimiser = torch.optim.Adam(\n"
    "    list(ml_adapters.parameters()) + list(lang_text_proj.parameters()),\n"
    "    lr=DUAL_LR\n"
    ")\n"
    "\n"
    "# Cache frozen XLM-R backbone features for all class prompts (computed once).\n"
    "# Only lang_text_proj changes during training; txt_backbone stays fixed.\n"
    "print(\"Caching frozen XLM-R backbone text features...\")\n"
    "with torch.no_grad():\n"
    "    txt_backbone = {}\n"
    "    for lang in LANGS:\n"
    "        prompts = [TEMPLATES[lang].format(TRANSLATIONS[c][lang]) for c in CLASSES]\n"
    "        tokens = tokenizer(prompts).to(device)\n"
    "        attn_mask = (tokens != model.text.config.pad_token_id).long()\n"
    "        out = model.text.transformer(input_ids=tokens, attention_mask=attn_mask)\n"
    "        masked = out.last_hidden_state * attn_mask.unsqueeze(-1).float()\n"
    "        pooled = masked.sum(dim=1) / attn_mask.sum(-1, keepdim=True).float()\n"
    "        txt_backbone[lang] = pooled.detach()  # [10, 768] -- static throughout training\n"
    "print(\"Done.\")\n"
    "\n"
    "# PGD against EN pair. txt_en must be detached so pixel gradient is isolated\n"
    "# from lang_text_proj parameters during PGD steps.\n"
    "def pgd_dual_en(x, y, txt_en, eps=DUAL_TRAIN_EPS, steps=DUAL_PGD_STEPS):\n"
    "    x0 = x.clone().detach()\n"
    "    alpha = 2.5 * eps / steps\n"
    "    xadv = torch.clamp(x0 + torch.empty_like(x0).uniform_(-eps, eps), 0, 1).detach()\n"
    "    for _ in range(steps):\n"
    "        xadv.requires_grad_(True)\n"
    "        feat = encode_image_ml(xadv, \"en\")\n"
    "        loss = F.cross_entropy(logits_for(feat, txt_en), y)\n"
    "        g = torch.autograd.grad(loss, xadv)[0]\n"
    "        with torch.no_grad():\n"
    "            xadv = torch.min(torch.max(xadv + alpha * g.sign(), x0 - eps), x0 + eps)\n"
    "            xadv = torch.clamp(xadv, 0, 1)\n"
    "        xadv = xadv.detach()\n"
    "    return xadv\n"
    "\n"
    "loss_hist = {\"ce\": [], \"div\": [], \"ret\": []}\n"
    "t0 = time.time()\n"
    "for epoch in range(DUAL_EPOCHS):\n"
    "    ep_ce, ep_div, ep_ret, n_batches = 0., 0., 0., 0\n"
    "\n"
    "    for xb, yb in dual_loader:\n"
    "        xb, yb = xb.to(device), yb.to(device)\n"
    "\n"
    "        # Current text embeddings — grad flows through lang_text_proj\n"
    "        txt_live = {\n"
    "            lang: F.normalize(lang_text_proj[lang](txt_backbone[lang]), dim=-1)\n"
    "            for lang in LANGS\n"
    "        }\n"
    "\n"
    "        # PGD vs EN pair — detach txt_en so PGD gradient only touches pixels\n"
    "        xb_adv = pgd_dual_en(xb, yb, txt_live[\"en\"].detach())\n"
    "\n"
    "        optimiser.zero_grad()\n"
    "\n"
    "        # Image features for all languages on clean batch\n"
    "        img_feats = {lang: encode_image_ml(xb, lang) for lang in LANGS}\n"
    "\n"
    "        # (1) Classification: sum_l CE(f_l(x) . g_l^T, y)\n"
    "        loss_ce = sum(\n"
    "            F.cross_entropy(logits_for(img_feats[lang], txt_live[lang]), yb)\n"
    "            for lang in LANGS\n"
    "        ) / len(LANGS)\n"
    "\n"
    "        # (2) Image divergence: sum_{l!=l'} cos(z_l, z_l')\n"
    "        orth, n_pairs = 0., 0\n"
    "        lang_list = list(img_feats)\n"
    "        for i in range(len(lang_list)):\n"
    "            for j in range(i + 1, len(lang_list)):\n"
    "                orth += (img_feats[lang_list[i]] * img_feats[lang_list[j]]).sum(dim=-1).mean()\n"
    "                n_pairs += 1\n"
    "        loss_div = orth / n_pairs\n"
    "\n"
    "        # (3) Adversarial retention: sum_{l!=en} CE(f_l(x_adv) . g_l^T, y)\n"
    "        # xb_adv is detached; gradient flows through non-EN adapters + text heads\n"
    "        loss_ret = sum(\n"
    "            F.cross_entropy(logits_for(encode_image_ml(xb_adv, lang), txt_live[lang]), yb)\n"
    "            for lang in LANGS if lang != \"en\"\n"
    "        ) / (len(LANGS) - 1)\n"
    "\n"
    "        loss = loss_ce + LAM_DIV * loss_div + LAM_RET * loss_ret\n"
    "        loss.backward()\n"
    "        optimiser.step()\n"
    "\n"
    "        ep_ce  += loss_ce.item()\n"
    "        ep_div += loss_div.item()\n"
    "        ep_ret += loss_ret.item()\n"
    "        n_batches += 1\n"
    "\n"
    "    loss_hist[\"ce\"].append(ep_ce / n_batches)\n"
    "    loss_hist[\"div\"].append(ep_div / n_batches)\n"
    "    loss_hist[\"ret\"].append(ep_ret / n_batches)\n"
    "    print(f\"Epoch {epoch+1:>2}/{DUAL_EPOCHS}  \"\n"
    "          f\"ce={ep_ce/n_batches:.3f}  \"\n"
    "          f\"div={ep_div/n_batches:.3f}  \"\n"
    "          f\"ret={ep_ret/n_batches:.3f}  \"\n"
    "          f\"({time.time()-t0:.0f}s)\")\n"
    "\n"
    "print(f\"\\nTraining complete in {time.time()-t0:.0f}s\")",
    "cell-train"))

